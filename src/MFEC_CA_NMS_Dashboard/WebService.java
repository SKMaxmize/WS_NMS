package MFEC_CA_NMS_Dashboard;

public class WebService {
	private String URLWebService;
	private String XMLWebService;
	private String MethodWebService;
	private String UsernameWebService;
	private String PasswordWebService;
	
	public String get_URLWebService(){
		return URLWebService;
	}
	
	public void set_URLWebService(String URLWebService){
		this.URLWebService = URLWebService;
	}

	public String get_XMLWebService(){
		return XMLWebService;
	}
	
	public void set_XMLWebService(String XMLWebService){
		this.XMLWebService = XMLWebService;
	}
	
	public String get_MethodWebService(){
		return MethodWebService;
	}
	
	public void set_MethodWebService(String MethodWebService){
		this.MethodWebService = MethodWebService;
	}
	
	public String get_UsernameWebService(){
		return UsernameWebService;
	}
	
	public void set_UsernameWebService(String UsernameWebService){
		this.UsernameWebService = UsernameWebService;
	}
	
	public String get_PasswordWebService(){
		return PasswordWebService;
	}
	
	public void set_PasswordWebService(String PasswordWebService){
		this.PasswordWebService = PasswordWebService;
	}
}
